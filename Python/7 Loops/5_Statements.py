# Break Statement
for i in range(100):
    if(i==34):
        break   # Exit the loop right now
    print(i)

# Continue Statement
for j in range(40):
    if(j==34):
        continue  # Skip this iteration
    print(j)

# Pass Statement (Pass is a null statement)
for k in range(100):
    pass        # for skip the statement

w = 0
while(w<15):
    print(w)
    w +=1

